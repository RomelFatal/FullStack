// Arrays in JavaScript
/*
  Arrays
  ------
  - An array allows you to store and work with multiple values of the same
    data type

  - An array is an ordered list of values.


  - Creating an array
  -----------------------
  const myArray = [];                 -> an empty array
  var car = ["Honda", "Kia", "Ford"]  -> an array with 3 values


  - To access a specific value in an array, we write its position in the
    array in square brackets (known as index).
  - The index of an array starts at 0.

  car[0] = "Honda";
  car[1] = "Kia";
  car[2] = "Ford";
*/
